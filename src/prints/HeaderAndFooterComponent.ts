// Transparent 1x1 PNG fallback when no logo is provided
const FALLBACK_LOGO =
    'data:image/png;base64,iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAYAAAAfFcSJAAAADUlEQVR42mNk+M9QDwADhgGAWjR9awAAAABJRU5ErkJggg==';

/**
 * Report Header
 * Full school header with logo, name, address, contact info, decorative line, title & subtitle.
 * Used as the first-page header; subsequent pages get a simplified version.
 */
export const getReportHeader = (
    schoolData: SchoolData,
    title: string,
    subtitle: string,
    pageOrientation: 'portrait' | 'landscape' = 'portrait',
    asContent: boolean = false
) => {
    // When rendered in header area, side margins are needed (no page margins there).
    // When rendered as content, page margins already handle side spacing.
    const side = asContent ? 0 : 30;
    const lineWidth = pageOrientation === 'landscape' ? 780 : 515;

    return {
        stack: [
            {
                columns: [
                    {
                        width: 70,
                        image: schoolData?.logo || FALLBACK_LOGO,
                        fit: [55, 55],
                    },
                    {
                        width: '*',
                        stack: [
                            {
                                text: schoolData?.schoolName || 'School Name',
                                style: 'schoolName',
                                alignment: 'center',
                            },
                            {
                                text: schoolData?.schoolAddress || 'School Address',
                                style: 'schoolAddress',
                                alignment: 'center',
                            },
                            {
                                text: [
                                    { text: 'Phone: ', bold: true },
                                    schoolData?.schoolPhone || 'N/A',
                                    { text: ' | Email: ', bold: true },
                                    schoolData?.schoolEmail || 'N/A',
                                ],
                                style: 'schoolContact',
                                alignment: 'center',
                            },
                        ],
                    },
                    {
                        width: 70,
                        text: '',
                    },
                ],
                margin: [side, 15, side, 0],
            },
            {
                canvas: [
                    {
                        type: 'line',
                        x1: 0,
                        y1: 3,
                        x2: lineWidth,
                        y2: 3,
                        lineWidth: 2,
                        lineColor: '#667eea',
                    },
                ],
                margin: [side, 3, side, 0],
            },
            {
                text: title,
                style: 'reportTitle',
                margin: [side, 5, side, 2],
            },
            {
                text: subtitle,
                style: 'reportSubtitle',
                margin: [side, 0, side, 5],
            },
        ],
    };
};

