/**
 * Signature Section
 * Renders signature lines with labels (default: Class Teacher + Principal).
 * Customizable via the `signatures` parameter.
 */
export const getSignatureSection = (signatures: SignatureConfig[] = []) => {
    const defaultSignatures: SignatureConfig[] = [
        { label: 'Class Teacher Signature', position: 'left' },
        { label: 'Principal Signature', position: 'right' },
    ];

    const sigs = signatures.length > 0 ? signatures : defaultSignatures;
    const columns: any[] = [];

    sigs.forEach((sig, index) => {
        const alignment =
            sig.position === 'left' ? 'left' : sig.position === 'right' ? 'right' : 'center';

        columns.push({
            width: '*',
            stack: [
                { text: '\n\n\n', fontSize: 8 },
                {
                    canvas: [{ type: 'line', x1: 0, y1: 0, x2: 120, y2: 0, lineWidth: 1 }],
                    alignment,
                },
                { text: sig.label, fontSize: 9, margin: [0, 5, 0, 0], alignment },
            ],
        });

        // Add spacer between signatures
        if (index < sigs.length - 1) {
            columns.push({ width: '*', text: '' });
        }
    });

    return {
        columns,
        margin: [0, 30, 0, 0],
    };
};

