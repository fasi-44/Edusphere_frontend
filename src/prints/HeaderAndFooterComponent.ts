/**
 * Format date to "DD MMM YYYY" (e.g. "15 Feb 2026")
 */
export const formatDate = (dateString: string): string => {
    const date = new Date(dateString);
    return date.toLocaleDateString('en-IN', {
        day: '2-digit',
        month: 'short',
        year: 'numeric',
    });
};

/**
 * Format date short - day only (e.g. "15")
 */
export const formatDateShort = (dateString: string): string => {
    const date = new Date(dateString);
    return date.toLocaleDateString('en-IN', {
        day: '2-digit',
    });
};

/**
 * Load pdfmake fonts (call once before generating any PDF)
 */
let fontsLoaded = false;

export const loadPdfFonts = async (): Promise<void> => {
    if (fontsLoaded) return;
    try {
        const pdfFontsModule = await import('pdfmake/build/vfs_fonts');
        const pdfMake = (await import('pdfmake/build/pdfmake')).default;

        if (pdfFontsModule.default && (pdfFontsModule.default as any).pdfMake) {
            pdfMake.vfs = (pdfFontsModule.default as any).pdfMake.vfs;
        } else if ((pdfFontsModule as any).pdfMake) {
            pdfMake.vfs = (pdfFontsModule as any).pdfMake.vfs;
        } else if (pdfFontsModule.default) {
            pdfMake.vfs = pdfFontsModule.default as any;
        } else {
            pdfMake.vfs = pdfFontsModule as any;
        }
        fontsLoaded = true;
    } catch (error) {
        console.warn('Could not load pdfmake fonts:', error);
    }
};
